import RegisterPage from '../pages/auth/register/register-page';
import LoginPage from '../pages/auth/login/login-page';
import HomePage from '../pages/home/home-page';
import NewPage from '../pages/new/new-page';
import StoriesDetailPage from '../pages/stories/stories-detail-page';
import DevProfilePage from '../pages/profile/dev-profile-page';
import { checkAuthenticatedRoute, checkUnauthenticatedRouteOnly } from '../utils/auth';


const routes = {
  '/login': () => checkUnauthenticatedRouteOnly(new LoginPage()),
  '/register': () => checkUnauthenticatedRouteOnly(new RegisterPage()),

  '/': () => checkAuthenticatedRoute(new HomePage()),
  '/new': () => checkAuthenticatedRoute(new NewPage()),
  '/stories/:id': () => checkAuthenticatedRoute(new StoriesDetailPage()),
  '/profile/developer': () => checkAuthenticatedRoute(new DevProfilePage()),
};

export default routes;
